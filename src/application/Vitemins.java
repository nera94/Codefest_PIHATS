package application;

public class Vitemins {
	
	String SID;
    String IID;
    long Time;
    int viteminLevel;
	public String getSID() {
		return SID;
	}
	public void setSID(String sID) {
		SID = sID;
	}
	public String getIID() {
		return IID;
	}
	public void setIID(String iID) {
		IID = iID;
	}
	public long getTime() {
		return Time;
	}
	public void setTime(long time) {
		Time = time;
	}
	public int getViteminLevel() {
		return viteminLevel;
	}
	public void setViteminLevel(int viteminLevel) {
		this.viteminLevel = viteminLevel;
	}

}
